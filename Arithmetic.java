public class Arithmetic {
    public static void main(String[] args) {
        //Arithmetic Operators
        int x = 20;
        int y = 10;
        int z = x + y;
        int z1 = x - y;
        int z2 = x * y;
        int z3 = x / y;
        int z4 = x % y;
        System.out.println("Sum of x + y = " + z);
        System.out.println("Sub of x - y = " + z1);
        System.out.println("Multi of x * y = " + z2);
        System.out.println("Div of x / y = " + z3);
        System.out.println("Mod of x % y = " + z4);
    }
}
